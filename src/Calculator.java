public class Calculator {

    public int addition(int first_number, int second_number) {
        int  amount = first_number + second_number;
        return amount;
    }

    public int subtraction(int first_number, int second_number) {
        int  amount = first_number - second_number;
        return amount;
    }

    public int multiplication(int first_number, int second_number) {
        int  amount = first_number * second_number;
        return amount;
    }

    public int divide(int first_number, int second_number) {
        int  amount = first_number / second_number;
        return amount;
    }

}
